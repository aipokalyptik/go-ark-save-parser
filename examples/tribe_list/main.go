package main

import (
	"fmt"
	"log"
	"os"

	"github.com/aipokalyptik/go-ark-save-parser/arkapi"
)

func main() {
	if len(os.Args) != 2 {
		log.Fatalf("usage: %s <save.ark-or-save-directory>", os.Args[0])
	}
	api, closeAPI, err := arkapi.NewPlayerFromPath(os.Args[1], arkapi.PlayerPathOptions{Fallback: arkapi.PlayerPathFallbackTribes})
	if err != nil {
		log.Fatal(err)
	}
	defer func() {
		if err := closeAPI(); err != nil {
			log.Fatal(err)
		}
	}()

	summary, err := api.TribeRosterSummary()
	if err != nil {
		log.Fatal(err)
	}
	fmt.Printf("tribes=%d with_names=%d members=%d dinos=%d\n", summary.Tribes, summary.WithNames, summary.Members, summary.Dinos)
}
